export function toTraditionalChinese(text: string): string {
  if (!text) return text;

  let result = text;

  // 第一阶段：词组替换（从长到短）
  const words = Object.keys(WORD_MAP).sort((a, b) => b.length - a.length);
  for (const word of words) {
    if (result.includes(word)) {
      result = result.split(word).join(WORD_MAP[word]);
    }
  }

  // 第二阶段：单字替换（处理未被词组覆盖的独立汉字）
  for (const [simp, trad] of Object.entries(CHAR_MAP_OVERRIDE)) {
    // 用词边界方式替换，避免部分匹配
    // 在汉字两侧查找
    result = result.replace(new RegExp(`([一-鿫])${simp}([一-鿫])`, 'g'), (_, before, after) => {
      return before + trad + after;
    });
  }

  return result;
}